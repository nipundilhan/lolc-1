package com.fusionx.lending.product.resources;

public class GeoResponse {
	
	private String geohiId;
	
	private String geohiParentId;
	
	private String geohiName;
	
	private String geohiStatus;
	
	private String geohiGeoLevelCode;

	public String getGeohiId() {
		return geohiId;
	}

	public void setGeohiId(String geohiId) {
		this.geohiId = geohiId;
	}

	public String getGeohiParentId() {
		return geohiParentId;
	}

	public void setGeohiParentId(String geohiParentId) {
		this.geohiParentId = geohiParentId;
	}

	public String getGeohiName() {
		return geohiName;
	}

	public void setGeohiName(String geohiName) {
		this.geohiName = geohiName;
	}

	public String getGeohiStatus() {
		return geohiStatus;
	}

	public void setGeohiStatus(String geohiStatus) {
		this.geohiStatus = geohiStatus;
	}

	public String getGeohiGeoLevelCode() {
		return geohiGeoLevelCode;
	}

	public void setGeohiGeoLevelCode(String geohiGeoLevelCode) {
		this.geohiGeoLevelCode = geohiGeoLevelCode;
	}
	
	

}
