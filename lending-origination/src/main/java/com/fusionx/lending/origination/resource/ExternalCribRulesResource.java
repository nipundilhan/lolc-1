package com.fusionx.lending.origination.resource;

public class ExternalCribRulesResource {
	
	private String referenceCode;

    private String conversionRequired;

    private String cribReportRequired;

	public String getReferenceCode() {
		return referenceCode;
	}

	public void setReferenceCode(String referenceCode) {
		this.referenceCode = referenceCode;
	}

	public String getConversionRequired() {
		return conversionRequired;
	}

	public void setConversionRequired(String conversionRequired) {
		this.conversionRequired = conversionRequired;
	}

	public String getCribReportRequired() {
		return cribReportRequired;
	}

	public void setCribReportRequired(String cribReportRequired) {
		this.cribReportRequired = cribReportRequired;
	}


    
    
}
